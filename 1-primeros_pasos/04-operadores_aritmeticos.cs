// Operadores aritméticos
int a = 10;
int b = 5;
int c;

Console.WriteLine("Operando los números " + a + " y " + b);
// Suma
c = a + b;
Console.WriteLine(a + " + " + b + " = " + c);

// Resta
c = a - b;
Console.WriteLine(a + " - " + b + " = " + c);

// Multiplicación
c = a * b;
Console.WriteLine(a + " * " + b + " = " + c);

// División
c = a / b;
Console.WriteLine(a + " / " + b + " = " + c);

// Módulo
c = a % 2;
Console.WriteLine(a + " % " + 2 + " = " + c);

