// Formas abreviadas de los operadores aritméticos
int a = 10;

Console.WriteLine("Formas abreviadas de los operadores aritméticos");
// Suma
a += 5;
Console.WriteLine(a);

// Resta
a -= 5;
Console.WriteLine(a);

// Multiplicación
a *= 5;
Console.WriteLine(a);

// División
a /= 5;
Console.WriteLine(a);

// Módulo
a %= 2;
Console.WriteLine(a);
